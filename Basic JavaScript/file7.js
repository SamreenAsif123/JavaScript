//Assignment Operator
//Assignment operators are used to assign values to the variables.
// They combine the assignment (=) operator with other operators to perform an operation while assigning a value.


let x = 5;
x+=6
console.log(x);


let y = 8
y-= 4
console.log(y);


let a=3
a*=6
console.log(a);


let b=6
b/=2
console.log (b);

let c=8
c%=5
console.log (c)

//These assignment operators provide a concise way to update variables based on arithmetic operations.
